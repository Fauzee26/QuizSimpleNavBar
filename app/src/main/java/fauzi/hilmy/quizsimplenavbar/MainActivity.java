    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_social) {
            Intent a1 = new Intent(getApplicationContext(), ActivityList.class);
            startActivity(a1);
        } else if (id == R.id.nav_buah) {
            Intent yy = new Intent(getApplicationContext(), ActivityRecycler.class);
            startActivity(yy);
        } else if (id == R.id.nav_profile) {
            Intent uytrew = new Intent(getApplicationContext(), ActivityProfile.class);
            startActivity(uytrew);
        } else if (id == R.id.nav_exit) {
            Context context = this;
            AlertDialog.Builder alertDialog = new AlertDialog.Builder(context);
            alertDialog.setTitle("Warning!!");
            alertDialog.setMessage("Apakah anda yakin akan keluar dari aplikasi ini?")
                    .setCancelable(false)
                    .setPositiveButton("Yes", new DialogInterface.OnClickListener() {
                        @Override
                        public void onClick(DialogInterface dialog, int which) {
                            //kondisi ketika kita pencet yes
                            MainActivity.this.finish();
                        }
                    })
                    .setNegativeButton("No", new DialogInterface.OnClickListener() {
                        @Override
                        public void onClick(DialogInterface dialog, int which) {
                            dialog.cancel();
                        }
                    });

            AlertDialog alert = alertDialog.create();
            alert.show();
        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }
